DROP DATABASE IF EXISTS cetis108_segundo_parcial;
CREATE DATABASE IF NOT EXISTS cetis108_segundo_parcial;
USE cetis108_segundo_parcial;

DROP TABLE IF EXISTS alumnos;
CREATE TABLE IF NOT EXISTS alumnos (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    no_control CHAR(14) NOT NULL UNIQUE,
    nombre VARCHAR(50) NOT NULL,
    paterno VARCHAR(30) NOT NULL DEFAULT 'X',
    materno VARCHAR(30) NOT NULL,
    curp CHAR(18) NOT NULL UNIQUE,
    genero ENUM('H','M') DEFAULT NULL,
    fecha_nacimiento DATE DEFAULT NULL,
    estado_2c CHAR(2) DEFAULT NULL,
    generacion CHAR(9) DEFAULT NULL
) ENGINE=InnoDB, CHARSET=utf8mb4, COLLATE=utf8mb4_general_ci;

DROP TABLE IF EXISTS carreras;
CREATE TABLE IF NOT EXISTS carreras (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(200) NOT NULL,
    nombre_2c CHAR(2) NOT NULL
) ENGINE=InnoDB, CHARSET=utf8mb4, COLLATE=utf8mb4_general_ci;

DROP TABLE IF EXISTS asignaturas;
CREATE TABLE IF NOT EXISTS asignaturas (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    carrera_id INT UNSIGNED NOT NULL,
    nombre VARCHAR(200) NOT NULL,
    semestre TINYINT(2) UNSIGNED NOT NULL,
    horas TINYINT(2) UNSIGNED DEFAULT NULL,
    KEY fk_asignaturas_carreras (carrera_id),
    CONSTRAINT fk_asignaturas_carreras
        FOREIGN KEY (carrera_id)
        REFERENCES carreras (id)
            ON UPDATE CASCADE
            ON DELETE RESTRICT
) ENGINE=InnoDB, CHARSET=utf8mb4, COLLATE=utf8mb4_general_ci;

DROP TABLE IF EXISTS grupos;
CREATE TABLE IF NOT EXISTS grupos (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    carrera_id INT UNSIGNED NOT NULL,
    semestre TINYINT(1) UNSIGNED NOT NULL,
    turno ENUM('MATUTINO','VESPERTINO') NOT NULL,
    nombre CHAR(6) NOT NULL,
    periodo VARCHAR(30) NOT NULL,
    UNIQUE KEY uk_nombre_periodo (nombre, periodo),
    KEY fk_grupos_carreras (carrera_id),
    CONSTRAINT fk_grupos_carreras
        FOREIGN KEY (carrera_id)
        REFERENCES carreras (id)
            ON UPDATE CASCADE
            on DELETE RESTRICT
) ENGINE=InnoDB, CHARSET=utf8mb4, COLLATE=utf8mb4_general_ci;

DROP TABLE IF EXISTS calificaciones;
CREATE TABLE IF NOT EXISTS calificaciones (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    grupo_id INT UNSIGNED NOT NULL,
    alumno_id INT UNSIGNED NOT NULL,
    asignatura_id INT UNSIGNED NOT NULL,
    parcial1 DOUBLE DEFAULT NULL,
    parcial2 DOUBLE DEFAULT NULL,
    parcial3 DOUBLE DEFAULT NULL,
    asistencias1 TINYINT(2) UNSIGNED DEFAULT NULL,
    asistencias2 TINYINT(2) UNSIGNED DEFAULT NULL,
    asistencias3 TINYINT(2) UNSIGNED DEFAULT NULL,
    tipo_acreditacion ENUM('A','NA','NP'),
    UNIQUE KEY uk_grupo_alumno_asignatura (grupo_id, alumno_id, asignatura_id),
    KEY fk_calificaciones_grupos (grupo_id),
    KEY fk_calificaciones_alumnos (alumno_id),
    KEY fk_calificaciones_asignaturas (asignatura_id),
    CONSTRAINT fk_calificaciones_grupos
        FOREIGN KEY (grupo_id)
        REFERENCES grupos (id)
            ON UPDATE CASCADE
            ON DELETE RESTRICT,
    CONSTRAINT fk_calificaciones_alumnos
        FOREIGN KEY (alumno_id)
        REFERENCES alumnos (id)
            ON UPDATE CASCADE
            ON DELETE RESTRICT,
    CONSTRAINT fk_calificaciones_asignaturas
        FOREIGN KEY (asignatura_id)
        REFERENCES asignaturas (id)
            ON UPDATE CASCADE
            ON DELETE RESTRICT
) ENGINE=InnoDB, CHARSET=utf8mb4, COLLATE=utf8mb4_general_ci;


